var pid_params = new ROSLIB.Topic({
  ros : rosServer,
  name : '/pid_params',
  messageType : 'pid_controller/PID'
});

var pidMsg = new ROSLIB.Message({
  kp : [0,0,0,0,0,0],
  ki : [0,0,0,0,0,0],
  kd : [0,0,0,0,0,0]
});

function pubPID_params(){
  // msg data order: kp,ki,kd
  pidMsg.kp[0] = Number(document.getElementById('kp_surge_slider').value) + Number(document.getElementById('kp_surge_slider_2').value);
  pidMsg.ki[0] = Number(document.getElementById('ki_surge_slider').value) + Number(document.getElementById('ki_surge_slider_2').value);
  pidMsg.kd[0] = Number(document.getElementById('kd_surge_slider').value) + Number(document.getElementById('kd_surge_slider_2').value);
  pidMsg.kp[1] = Number(document.getElementById('kp_sway_slider').value) + Number(document.getElementById('kp_sway_slider_2').value);
  pidMsg.ki[1] = Number(document.getElementById('ki_sway_slider').value) + Number(document.getElementById('ki_sway_slider_2').value);
  pidMsg.kd[1] = Number(document.getElementById('kd_sway_slider').value) + Number(document.getElementById('kd_sway_slider_2').value);
  pidMsg.kp[2] = Number(document.getElementById('kp_heave_slider').value) + Number(document.getElementById('kp_heave_slider_2').value);
  pidMsg.ki[2] = Number(document.getElementById('ki_heave_slider').value) + Number(document.getElementById('ki_heave_slider_2').value);
  pidMsg.kd[2] = Number(document.getElementById('kd_heave_slider').value) + Number(document.getElementById('kd_heave_slider_2').value);
  pidMsg.kp[3] = Number(document.getElementById('kp_roll_slider').value) + Number(document.getElementById('kp_roll_slider_2').value);
  pidMsg.ki[3] = Number(document.getElementById('ki_roll_slider').value) + Number(document.getElementById('ki_roll_slider_2').value);
  pidMsg.kd[3] = Number(document.getElementById('kd_roll_slider').value) + Number(document.getElementById('kd_roll_slider_2').value);
  pidMsg.kp[4] = Number(document.getElementById('kp_pitch_slider').value) + Number(document.getElementById('kp_pitch_slider_2').value);
  pidMsg.ki[4] = Number(document.getElementById('ki_pitch_slider').value) + Number(document.getElementById('ki_pitch_slider_2').value);
  pidMsg.kd[4] = Number(document.getElementById('kd_pitch_slider').value) + Number(document.getElementById('kd_pitch_slider_2').value);
  pidMsg.kp[5] = Number(document.getElementById('kp_yaw_slider').value) + Number(document.getElementById('kp_yaw_slider_2').value);
  pidMsg.ki[5] = Number(document.getElementById('ki_yaw_slider').value) + Number(document.getElementById('ki_yaw_slider_2').value);
  pidMsg.kd[5] = Number(document.getElementById('kd_yaw_slider').value) + Number(document.getElementById('kd_yaw_slider_2').value);

  pid_params.publish(pidMsg);
}

var pid_setpoints = new ROSLIB.Topic({
  ros : rosServer,
  name : '/setpoints',
  messageType : 'pid_controller/Setpoint'
});

var spMsg = new ROSLIB.Message({
  setpoints : [0,0,0,0,0,0]
});

function pubPID_setpoints(){
  spMsg.setpoints[0] = Number(document.getElementById('sp_surge_slider').value) + Number(document.getElementById('sp_surge_slider_2').value);
  spMsg.setpoints[1] = Number(document.getElementById('sp_sway_slider').value) + Number(document.getElementById('sp_sway_slider_2').value);
  spMsg.setpoints[2] = Number(document.getElementById('sp_heave_slider').value) + Number(document.getElementById('sp_heave_slider_2').value);
  spMsg.setpoints[3] = 0.0;
  spMsg.setpoints[4] = 0.0;
  spMsg.setpoints[5] = Number(document.getElementById('sp_yaw_slider').value) + Number(document.getElementById('sp_yaw_slider_2').value);
  
  pid_setpoints.publish(spMsg);
}
